// MARK: - Implementation Details

extension KLocalization {
  private static func tr(_ table: String, _ key: String, _ args: CVarArg...) -> String {
    let format = BundleToken.bundle.localizedString(forKey: key, value: nil, table: table)
    return String(format: format, locale: Locale.current, arguments: args)
  }
  public static func localizable(_ key: String, checkInApp: Bool = false, _ args: CVarArg...) -> String {
    if checkInApp {
      return Bundle.main.localizedString(forKey: key, value: tr("OCLocalizable", key, args), table: nil)
    } else {
      return tr("OCLocalizable", key, args)
    }
  }
}

// swiftlint:disable convenience_type
private final class BundleToken {
  static let bundle: Bundle = {
    #if SWIFT_PACKAGE
    return Bundle.module
    #else
    return Bundle(for: BundleToken.self)
    #endif
  }()
}
